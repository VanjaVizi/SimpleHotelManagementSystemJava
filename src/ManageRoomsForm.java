    private void jButtonRemoveRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveRoomActionPerformed
        try{
            int rn=Integer.valueOf(jTextFieldNumber.getText());
            if(room.removeRoom(rn)){
                JOptionPane.showMessageDialog(rootPane, "Room deleted successfuly", "Delete room", JOptionPane.INFORMATION_MESSAGE);
            }else{
                 JOptionPane.showMessageDialog(rootPane, "Room not deleted", "Delete room error", JOptionPane.ERROR_MESSAGE);
            }
                
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage()+" Enter room number", "Room number error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButtonRemoveRoomActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
           try{
               //get data 
                int number = Integer.valueOf(jTextFieldNumber.getText());
                int type = Integer.valueOf(jComboBoxType.getSelectedItem().toString());
                String phone = jTextFieldPhone.getText();
                String reserved="NO";
                if(jRadioButtonY.isSelected()){
                    reserved="YES";
                }
                        
                if(phone.trim().equals("")){
                    JOptionPane.showMessageDialog(rootPane, "Fill all fields", "Edit room error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if(room.editRoom(type,phone,reserved,number)){
                    JOptionPane.showMessageDialog(rootPane, "Room edited successfuly", "Edit room", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Room not edited", "Edit room error", JOptionPane.ERROR_MESSAGE);
                }
               
               
           }catch(Exception e){
               JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Edit room error", JOptionPane.ERROR_MESSAGE);
           }
       
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonAddNewRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddNewRoomActionPerformed

        
        try{
            //get data
            int number = Integer.valueOf(jTextFieldNumber.getText());
            int type = Integer.valueOf(jComboBoxType.getSelectedItem().toString());
            String phone = jTextFieldPhone.getText();
            if(phone.trim().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Fill all fields", "Add room error", JOptionPane.ERROR_MESSAGE);
            return;
            }
            if(room.addRoom(number,type,phone)){
                JOptionPane.showMessageDialog(rootPane, "New room added successfuly", "Add room", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Room not added", "Add room error", JOptionPane.ERROR_MESSAGE);
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Add room error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAddNewRoomActionPerformed

