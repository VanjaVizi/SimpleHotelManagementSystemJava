    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       
        //get the jtable model
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        //get the selected row index
        int rowIndex= jTable1.getSelectedRow();
        
        //display data
        jTextFieldNumber.setText(model.getValueAt(rowIndex,0).toString());
        jComboBoxType.setSelectedItem(model.getValueAt(rowIndex, 1));
        jTextFieldPhone.setText(model.getValueAt(rowIndex,2).toString());
        
        String reserved = model.getValueAt(rowIndex, 3).toString();
        if(reserved.equals("NO")){
            jRadioButtonY.setSelected(false);
            jRadioButtonN.setSelected(true);
        }else{
            jRadioButtonY.setSelected(true);
            jRadioButtonN.setSelected(false);
        }
        
        

    }//GEN-LAST:event_jTable1MouseClicked

